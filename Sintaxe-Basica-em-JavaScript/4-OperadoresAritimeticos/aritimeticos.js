var a = 5 + 3;         // = 8
var b = a % 5;         // = 3
var c = 5 * b ** 2;    // = 45
var d = 10 - a / 2;    // = 6
var e = 6 * 2 / d;     // = 2
var f = b % e + 4 / e; // = 3


console.log("5 + 3         : " + a);

console.log("8 % 5         : " + b);

console.log("5 * 3 ** 2    : " + c);

console.log("10 - 8 / 2    : " + d);

console.log("6 * 2 / 6     : " + e);

console.log("3 % 2 + 4 / 2 : " + f);


